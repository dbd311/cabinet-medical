<div class="mt-3"><strong>Après la consultation</strong></div>
<p class="m-t-1">À la suite d’une séance d’ostéopathie, le corps du patient doit s’adapter et retrouver son équilibre. Parfois, le patient a une sensation de fatigue et c'est tout à fait normale. En effet, les résultats du traitement sont significatifs quelques jours voire quelques semaines après la consultation.</p>
<p>Durant les deux ou trois premiers jours suivants la consultation, il est possible que la plainte du patient persiste et même se projette à d’autres endroits du corps. Tout ceci est normal, cette réaction passagère signe l'adaptation du corps au traitement. Au-delà de cette période, la douleur s’estompe et disparaît jusqu’à ce que le corps se stabilise dans un état qui sera le vrai résultat de la séance. Cela peut parfois prendre deux ou trois semaines, mais encore une fois ceci est propre à chaque individu. Si l’amélioration du motif de consultation n’a été que passagère, ou est incomplète, c’est le bon moment pour reprendre rendez-vous.</p>
<p> Le nombre de consultations dépend du motif pour lequel vient le patient, mais en général il faut compter de un à trois consultations. Lors du suivi d’un patient, il est conseillé d'espacer les séances de quelques semaines. Dans ces conditions, le patient peut attendre à garder un bénéfice durable de son traitement ostéopathique.
</p>

