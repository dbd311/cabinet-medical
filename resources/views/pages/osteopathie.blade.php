<p>
    Par une prise en charge du patient dans sa globalité, l'ostéopathe a pour but de rétablir l'harmonie mécanique, en s'appuyant sur l'anatomie, la physiologie et la biomécanique humaine.
</p>
<p>
    Ainsi, l'ostéopathe aide le corps à retrouver sa capacité d'auto-guérison et d'adaptation qui lui est propre.
</p>
<blockquote class="blockquote blockquote-reverse border-purple">
    <h6><p>« L'ostéopathie est la loi de l'esprit, de la matière et du mouvement »</p>                    
        <blockquote-footer class="text-muted">Andrew Taylor Still, Autobiographie, Vannes, Sully, 1998, p.208.</blockquote-footer></h6>
</blockquote>

<p>
    Selon l'<a href="http://www.osteopathic.org/Pages/default.aspx">Association Ostéopathique Américaine</a> (AOA), l'<b>ostéopathie</b> est un <b>système de soins médical</b> avec une philosophie qui combine les <b>besoins du patient</b> avec la <b>science médicale courante</b>, une mise en valeur de l'interrelation entre la <b>structure</b> et la <b>fonction</b> et une <b>reconnaissance</b> de la <b>capacité du corps</b> à s'<b>autoguérir</b>.
</p>

<p>
    Cette approche holistique des soins et de la guérison des patients se fonde sur les trois principes suivantes :
</p>
<ol>
    <li>
        L'unité du corps humain : 
        <p>
            Les différents systèmes du corps travaillent en interdépendance les uns avec les autres car le coprs humain est formé d'un continuum anatomique. L'ostéopathie s'appuie sur la compréhension de ces mécanismes physiologiques complexes pour rechercher la cause d'une pathologie et non ses seules conséquences.
        </p>

    </li>

    <li>
        La structure gouverne la fonction et vice-versa :
        <p>
            La structure du corps dirige la fonction (par exemple l'anatomie de la hanche détermine ses divers degrés de mobilité). La fonction influence la structure (par exemple une danseuse pratiquant depuis quelques années n'aura pas la même hanche qu'un sédentaire et encore moins qu'un footballeur).

            Il y a donc bel et bien une interdépendance entre la Fonction et la Structure.
        </p>

    </li>

    <li>
        L'homéostasie :
        <p>
            C'est la capacité de tout organisme vivant à s'autoréguler, s'adapter et s'auto guérir. Sans cette capacité la vie serait impossible. Le travail de l'ostéopathe consiste à libérer le mouvement des structures visant à rendre au patient son potentiel optimal d'adaptation.
        </p>
    </li>
</ol>            



