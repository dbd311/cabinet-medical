<div class="section-title"><a href="/osteopathie">L'ostéopathie</a></div>
<p>
    Fondée par <a href="https://fr.wikipedia.org/wiki/Andrew_Taylor_Still">Andrew Taylor Still</a> (1828-1917), l'ostéopathie est une thérapie uniquement manuelle qui s'emploie à déterminer et à traiter toute restriction de mobilité des différentes structures composant le corps humain. En 1892, son fondateur créa la première école indépendante d'ostéopathie aux Etats-Unis. En France, l'ostéopathie a été pratiquée et enseignée à partir des années 1950, au début via des thérapeutes qui s'étaient formés au Royaume-Uni.
</p>
<p>
    La reconnaissance de l'ostéopathie par le droit Français est datée du début de ce siècle (mentionnée par l'article 75 de la loi n° <a href="https://www.legifrance.gouv.fr/affichTexteArticle.do;jsessionid=4B15DB841449B1900A0373490D394D1F.tpdila17v_2?idArticle=LEGIARTI000023857681&cidTexte=LEGITEXT000005632379&dateTexte=20150224">2002-303</a> du 4 mars 2002 relative aux droits des malades et à la qualité du système de santé).        
</p>

