<p>Constamment sollicitées, les articulations sont fréquemment sujettes à une inflammation ou à une détérioration du cartilage. Les maladies articulaires sont très handicapantes, douloureuses et parfois même invalidantes.</p>
<p>L’ostéopathie articulaire a pour but d’équilibrer et de redonner de la mobilité à des zones articulaires telle qu’une cheville, un genou, un bassin ou au niveau de la colonne vertébrale. Elle dispose de plusieurs techniques pour traiter le système ostéo-articulaire. Chacune des séries de manipulations est adaptée au patient, à sa pathologie, à son âge et à son mode de vie. Le but n’est pas de choquer votre corps mais de l’aider à retrouver sa mobilité en cherchant à atteindre petit à petit, la barrière motrice : l’amplitude maximale du mouvement d’une articulation. </p>
<p>Il est important de comprendre que l’ostéopathie articulaire n’est qu’une technique de correction pour redonner de la mobilité à une zone vertébrale ou articulaire. Elle ne peut en aucun cas être l’unique technique utilisée pour constituer un traitement.</p>
<p> L’ostéopathie articulaire n’est pas dangereuse lorsqu’elle est bien pratiquée et pour cela, un bilan radiologique est souvent recommandé afin d’effectuer un geste sûr et sans risque.</p>

