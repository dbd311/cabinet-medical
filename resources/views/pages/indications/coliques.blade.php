        <p>Dans la liste des problèmes de santé du nourrisson, les coliques arrivent en deuxième place après les régurgitations. Les coliques se présentent chez 10 % à 40 % des bébés et elles se manifestent selon les symptômes suivants :</p>
        <ul>
            <li>le ventre est très gonflé, tendu et dur,</li>
            <li>les mains sont crispées,</li>
            <li>les genoux sont repliés sur le ventre,</li>
            <li>l'expression du visage et les pleurs expriment souvent la détresse du bébé,</li>
            <li>des gaz assez puissants peuvent se manifester lorsque le bébé essaie de bouger pour se libérer de ses tensions.</li>
        </ul>
        <p>Pour poser un diagnostic probable de coliques, il existe une « <em>règle de trois</em> » :  
            Le bébé pleure plus de <strong>3 heures par jour</strong>, plus de <strong>3 jours par semaine</strong> et pour plus de <strong>3 semaines</strong>. 
            Les pleurs ou « crises de coliques » sont souvent concentrés en soirée et surviennent comme des crises spontanées imprévisibles et prolongées. En dehors des crises de coliques, le bébé semble heureux, dort bien et sa prise de poids est continue. Typiquement les coliques commencent autour de la troisième semaine de vie et se prolongent jusqu’au 3e ou 4e mois.</p>
        
