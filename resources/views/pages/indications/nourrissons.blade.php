<div class="section-title"><a href="/indications/osteopathie-et-nourrissons">L'ostéopathie pour le nourrisson</a></div>

<img class="img-fluid" src="/images/osteopathie/indications/osteo-et-nourrisson.jpg" alt="ostéopathie et nourrisson"/>

<p class="mt-1">
    Pour le <a href="/indications/osteopathie-et-nourrissons">nourrisson</a>, la naissance est une expérience puissante et modelante. Dès la naissance, certains bébés présentent des torticolis congénitaux, des troubles digestifs ou des problèmes de sommeil... Gênants, ces tracas de santé, et du quotidien, trouvent la plupart du temps leurs origines dans une mauvaise position du petit in utero ou dans des manœuvres d'extractions délicates au moment de l'accouchement.
</p>
<p>
    Les tensions que l’enfant présente à la naissance ne sont pas uniquement dues à l’accouchement mais aussi à des pressions subies pendant la grossesse. Ces contraintes peuvent rester inscrites dans les tissus de l'enfant sous forme de tensions pouvant par la suite lui occasionner des symptômes divers.
</p>
<p>
    <strong>L’ostéopathie</strong> présente un intérêt tout particulier dans le traitement des troubles du nourrisson. Pour l’ostéopathe, les informations relatives au déroulement de la grossesse, à l’accouchement et à la période périnatale sont essentielles. Les désordres liés à ces différentes étapes ne doivent pas s’inscrire de façon durable dans le temps, c’est pourquoi il semble nécessaire d’adopter une attitude préventive et thérapeutique auprès des nouveau-nés. 
</p>
<p>
    Après la naissance, il est donc conseillé d'amener votre enfant chez l'ostéopathe afin de faire une séance de contrôle ou un bilan. Avec douceur et précision, l’examen ostéopathique permet de vérifier l’équilibre entre les systèmes osseux, organiques et musculaires de l’enfant et de les réajuster avant l’apparition de symptômes ou de maladies.
</p>

<p>
    <strong>Quand consulter un ostéopathe ?</strong>
</p>

<p>
    Si le bébé est né :
</p>
<ul>
    <li>lors d’un accouchement difficile : une extraction instrumentale (forceps, ventouse, spatules), une césarienne (programmée ou non).</li>
    <li> avec le cordon ombilical enroulé autour du cou du bébé.</li>
    <li>avec le crâne déformé par l’utilisation de forceps ou de spatules.</li>
</ul>

<p> Et si le bébé :</p>
<ul>
    <li>a été saisi par les pieds et suspendu la tête en bas.</li>
    <li>régurgite, dort peu ou pleure souvent.</li>
    <li>présente une respiration bruyante ou des difficultés à s’alimenter.</li>
    <li>se cambre en arrière durant la tétée.</li>
    <li>se tient en « virgule » et tourne la tête plus facilement d’un côté.</li>
    <li>présente un méplat à l’arrière de la tête.</li>
    <li>a des coliques, des signes de nervosité, d’hypertonicité ou d’apathie.</li>
    <li>est constipé.</li>
    <li>présente une obstruction partielle du canal lacrymal.</li>
    <li>est sujet à des otites, des rhinites ou des bronchiolites à répétition, des signes de torticolis congénital, un strabisme, etc.
    </li>                
</ul>

