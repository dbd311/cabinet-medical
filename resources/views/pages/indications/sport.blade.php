<div class="row">
    <div class="col-8"> 
        <p>            
            Le Sport et l'Ostéopathie sont unis par un lien indissociable. Avant et pendant l'épreuve, le sportif a besoin de cette pratique manuelle pour entretenir sa forme. 
            Durant l'exercice, un déséquilibre est susceptible de s'installer et peut se présenter différemment, une simple contracture ou une douleur chronique.
            Les ligaments, les muscles ainsi que les articulations sont soumis à rude épreuve durant l'activité et cette discipline est là pour maintenir la souplesse 
            articulaire et la capacité respiratoire.
        </p>
        <p>
            Toute perte de mobilité des articulations, des muscles, des ligaments ou des viscères peut provo-quer un déséquilibre de l’état de santé. 
            Celui-ci se manifeste chez le sportif par différents symptômes, de la simple contracture à la douleur chronique, altérant de toute façon les performances.
        </p>
        <p>    L'organisme d'un sportif de haut niveau est comparable à un système d'horlogerie où le moindre grain de sable vient enrayer la bonne marche de l'activité physique intense. 
            Il n'est plus à démontrer l'intérêt de l'Ostéopathie dans l'équilibre du sportif et l'amélioration puis le maintien de ses performances. 
            Ainsi, de plus en plus de sportifs, de centres de sports, d’associations sportives, de fédérations et de centres de soins font appel à l’expertise de 
            l’ostéopathe.
        </p>

        À la demande d'organisations sportives, des ostéopathes interviennent sur des manifestations sportives de renommée comme : 
