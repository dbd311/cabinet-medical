<div class="section-title"><a href="/indications/osteopathie-et-handicap">Ostéopathie et handicap</a></div>

<div class="row">
    <div class="col-8"> 
        <p> L’handicap est une notion extrêmement large ayant pour conséquence une diminution de l’<strong>autonomie</strong> qu’elle soit physique ou mentale. La situation de handicap va avoir une multitude de conséquences sur le corps que ce soit à cause de différences de tonus musculaires, de déformations (scoliose essentiellement) ou d’appareillages (fauteuils roulants) qui vont provoquer des <strong>douleurs</strong> ou des <strong>inconforts</strong> (inconfort digestif).</p>
        <p> Dans de tels cas, l’ostéopathie, si elle ne soigne pas la source du problème, est une aide précieuse. En effet, elle permet d’établir sur le plan fonctionnel, les meilleures conditions possibles pour aider l’organisme. Pour cela, l’ostéopathie rééquilibre chez les personnes porteuses d’un handicap les tensions pathologiques liées au handicap quel qu’il soit et libère du stress quotidien qui s’y rajoute. Souvent, le simple fait de recouvrer un meilleur potentiel permet des dépassements et des progrès inattendus. Les séances apportent ainsi une aide à l'évolution de ces personnes aussi bien dans leur développement psychique que dans leur confort physique, émotionnel et relationnel.</p>
        <p> Pour finir, l’ostéopathie n’est, bien entendu, pas une méthode exclusive, mais doit s’inscrire dans un cadre thérapeutique faisant appel à tout ce qui peut aider l’individu à surmonter son handicap.</p>
