<div class="section-title"><a href="/indications/osteopathie-et-adultes">L'ostéopathie pour les adultes</a></div>

<div class="row">
    <div class="col-7"> 
        <p>Le travail (avec ses attitudes posturales adoptées et ses micro-traumatismes répétés), les activités sportives, les loisirs, le stress et le surmenage, etc.
            sont autant de <strong>facteurs irritatifs</strong> auquel le corps adulte devra <strong>s’adapter en permanence</strong>.</p>
        <p>
            Les antécédents traumatiques, pathologiques, chirurgicaux peuvent créer des troubles de la posture, ou de la mobilité articulaire. Ces troubles s’expriment volontiers lors des périodes de fatigue, qui ne sont pas rares chez l’adulte actif. 
        </p>
    </div>
    <div class="col-5"><img class="img-fluid" src="/images/osteopathie/indications/osteo-et-adulte.jpg" alt="ostéopathie et adulte" /></div>
</div>
<p>De part sa démarche, l'ostéopathie est un outil précieux afin que les patients prennent conscience de leurs mauvaises postures et, aide à ce que les douleurs liées n'apparaissent plus.</p>
<p>Ainsi il est conseillé de consulter un ostéopathe pour les motifs suivants :</p>
<ul>
    <li>Bilan préventif annuel,</li>
    <li>Douleurs du dos,</li>
    <li>Tendinites,</li>
    <li>Lumbagos (chronique ou en crise),</li>
    <li>Douleurs du cou,</li>
    <li>Sciatiques et Sciatalgies,</li>
    <li>Céphalées et Migraines,</li>
    <li>Pathologies ORL chroniques,</li>
    <li>Douleurs rhumatismales (prise en charge à visée préventive ou pour soulager des poussées inflammatoires),</li>
    <li>Douleurs musculaires (crampes, contractures),</li>
    <li>Aide au rétablissement après une intervention chirurgicale (consulter 3 semaines après l’intervention afin de permettre une cicatrisation correcte),</li>
    <li>Accompagnement pour les pathologies lourdes afin d’en soulager des douleurs, diminuer l’importance des atteintes fonctionnelles liés aux symptômes de la maladie.</li>
    <li>Problèmes digestifs :
        <ul>
            <li>reflux acides,</li>
            <li>pesanteur d’estomac,</li>
            <li>brûlure d’estomac,</li>
            <li>troubles du transit (diarrhée – constipation),</li>
            <li>colites spasmodiques (colopathies fonctionnelles) ...</li>
        </ul>
    </li>
    <li>Problèmes de femmes :
        <ul>
            <li>douleur au bas ventre, au dos, dans les jambes ou dans les fesses pendant les règles,</li>
            <li>irrégularités du cycle menstruel ...</li>                        
        </ul>
    </li>

</ul>
