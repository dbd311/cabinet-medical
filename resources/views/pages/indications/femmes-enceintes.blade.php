<div class="section-title"><a href="/indications/osteopathie-et-femmes-enceintes">L'osteopathie pour les femmes enceintes</a></div>

<div class="row">
    <div class="col-8"> 
        <p>La métamorphose de la femme enceinte peut engendrer quelques désagréments.
            En se développant, le fœtus  va parfois comprimer certaines structures comme la vessie par exemple.
        </p>
        <p>
            Au cours de la grossesse, la maman adapte 
            sa posture afin de soulager les tensions
            qu’elle ressent dues au poids du bébé grandissant.
            Le centre de gravité est ainsi déplacé ce qui la
            tracte vers l’avant. C'est pourquoi il peut apparaître différents maux tels que douleurs au niveau du <a href="/indications/mal-de-dos">dos</a>, du bassin, des hanches, des côtes ou des sciatiques ...</p>
    </div>
    <div class="col-4"><img class="img-fluid" src="/images/osteopathie/indications/osteo-et-femme-enceinte.jpg" alt="ostéopathie et femme enceinte" /></div>
</div>
<p>Si ces douleurs sont typiques de la grossesse elles ne sont pas une fatalité pour autant.</p>
<p>Pour toutes ces raisons nous conseillons vivement aux femmes de consulter pendant et après leur grossesse.</p>
<p>Toutes les précautions sont prises afin que le traitement mis en place pendant la séance ne soit nocif ni pour le bébé ni pour la maman. Des techniques toutes en douceur et adaptées existent afin de soulager en toute sécurité la patiente.</p>
<p>Évidemment, une <a href="/consultation">consultation d'ostéopathie</a> ne se substitue pas à un suivi régulier et sérieux effectué par un gynécologue-obstétricien comme il est de mise dans le cadre de toute grossesse.</p>

