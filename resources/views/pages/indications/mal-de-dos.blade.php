<div class="section-title"><a href="/indications/mal-de-dos">Ostéopathie et le mal de dos</a></div>

<div class="row">
    <div class="col-5"> 
        <p>Le <strong>mal de dos</strong> ou la <em>rachialgie</em> désigne toute douleur siégeant au niveau du rachis (c-à-d la colonne vertébrale) ou à proximité de celui-ci, quelles que soient l’origine, la nature et l’intensité de la douleur. En fonction du point d'origine de la douleur, on distingue les cervicalgies, les dorsalgies, les lombalgies et les névralgies sciatiques (cf. la photo). </p>
    </div>
