<div class="row">
    <div class="col-xs-12">
        <p>En général, l’origine du mal de dos est souvent mal identifiée dans 95 % des cas. Cependant, de nombreuses causes y sont responsables : la mauvaise position ou posture en voiture ou au bureau, les mouvements brusques, le sport intensif, le stress, la fatigue ou encore les problèmes viscéraux… Tout ceux-ci qui provoquent des tentions musculaires et ligamentaires donnent lieu à des contractures douloureuses.</p>
        <p>Les <strong>lombalgies</strong> sont les plus fréquentes, en particulier chez les travailleurs manuels tandis que les cervicalgies touchent davantage les travailleurs intellectuels dans les bureaux. Les causes des lombalgies sont diverses : lésion à un muscle, un tendon ou un ligament; dégénérescence ou une hernie discale; glissement d’une vertèbre sur une autre vertèbre; règles douloureuses ou d'autres raisons gynécologiques; vieillesse; etc. </p>
        <p>Si la douleur au dos survient brutalement, c'est généralement le signe d'une déchirure musculaire, d'une entorse ligamentaire ou d'un problème de disque. La pression qui s'exerce sur le disque peut le faire gonfler et provoquer sa rupture, poussant le cartilage sur les côtés. Le cartilage presse alors sur le cordon nerveux de la colonne, provoquant une douleur intense. </p>
        <p>De nombreuses affections comme l'ostéoporose, l'arthrose, la fibromyalgie, la spondylarthrite ankylosante et la sténose du canal rachidien peuvent provoquer une douleur au dos. La grossesse peut également provoquer une douleur au dos à cause du surplus de poids, des changements dans la fermeté des muscles et des ligaments, et aussi parce que la tête du bébé peut comprimer les nerfs de la colonne. Les sujets présentant une surcharge pondérale sont également plus prédisposés à avoir des douleurs au dos, à cause d'une posture moins bonne et de la pression supplémentaire qui s'exerce sur les disques et les muscles.</p>
        <p>En ce qui concerne les <strong>dorsalgies</strong>, plusieurs causes principales peuvent être citées. Ainsi, elles peuvent survenir à la suite :</p>
        <ul>
            <li>D’un mauvais maintien du dos au quotidien ou d’une mauvaise posture;</li>
            <li>De problèmes musculaires comme l’affaiblissement ou l’élongation des muscles du dos;</li>
            <li>D’atteintes articulaires comme l’arthrose, la hernie discale,  la spondylarthrite ankylosante, etc;</li>
            <li>D’une fracture spontannée occasionnée par l’ostéoporose ou par un traumatisme;</li>
            <li>Des efforts musculaires ou des gestes répétitifs.</li>
        </ul>
        <p>De plus, il existe des facteurs aggravants qui peuvent favoriser la dorsalgie, parmi ceux-ci, on peut noter :</p>
        <ul>
            <li>Le surpoids et/ou les femmes en gestation;</li>
            <li>L’âge, dont les femmes ménopausées;</li>
            <li>La sédentarité comme le fait d’être longuement assis ou alité par exemple;</li>
            <li>Le manque d’activités physiques;</li>
            <li>Ainsi que d’autres facteurs qui influencent de manière directe ou indirecte la qualité de vie du sujet comme entre autre: le tabagisme, le stress, l’anxiété, les mauvaises habitudes alimentaires.</li>
        </ul>
