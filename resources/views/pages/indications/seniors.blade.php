<div class="section-title"><a href="/indications/osteopathie-et-seniors">L'ostéopathie chez les seniors</a></div>

<div class="row">
    <div class="col-7"> 
        <p>Les années passent et inexorablement apparaissent la raideur. La réalisation des gestes simples de la vie quotidienne devient difficile à réaliser et à coordonner. L’ostéopathie par sa pratique analytique permet de stabiliser, ralentir et d’améliorer la douleur et la gêne fonctionnelle de la plupart des phénomènes dégénératifs bénins dû à l’âge et à la sédentarité.  </p>
        <p>La prise en charge ostéopathique précoce et régulière permet aux séniors de gagner en fonctionnalité en ayant une action sur la dimension physique de manière durable. De la même manière, le traitement ostéopathique diminue fortement les douleurs ressenties sur le long terme et ainsi participe à l’amélioration de la qualité de vie.</p>
    </div>
    <div class="col-5"><img class="img-fluid" src="/images/osteopathie/indications/osteo-et-senior.jpg" alt="ostéopathie et senior" /></div>
</div>


<p><strong>Quand solliciter un ostéopathe ?</strong></p>
<ul>
    <li><strong>A titre préventif</strong>
        <p>Afin de diagnostiquer et stabiliser les zones de faiblesse de votre corps et d’optimiser les facultés de mobilité, de coordination gestuelle et de minimiser le risque de chute. </p>
        <p>Pour faire un bilan, en accompagnement de l’arrêt de l’activité professionnelle, avant le démarrage d’une activité sportive.</p>
    </li>
    <li><strong>A titre curatif</strong>
        <p>Dans le cas par exemple de : <em>périarthrite de l’épaule</em>, <em>lombo-sciatique</em>, <em>cervicalgie</em>, dans l’ensemble des phénomènes dégénératifs articulaires provoquant des troubles de la statique et des douleurs, sensation de perte d’équilibre et de pseudo-vertige, de diminuer la prise d’antalgique et d’anti-inflammatoire.
        </p>
        <p> Enfin, l’ostéopathie par son approche novatrice permet de stabiliser et de retrouver un confort fonctionnel au quotidien (marche, sport, activités culturelles et familiales), le traitement s’accompagnera de conseils et d’exercices afin de pérenniser les bienfaits de la séance.</p>
    </li>                
</ul>
