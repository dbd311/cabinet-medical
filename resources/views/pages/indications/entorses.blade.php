<div class="section-title"><a href="/indications/mal-de-dos">Entorse</a></div>

<div class="row">
    <div class="col-5"> 
        L'<strong>entorse</strong> est une lésion traumatique touchant une articulation et responsable d'un étirement ou d'une rupture des ligaments. Un mouvement brutal ou un traumatisme en est la cause. Il existe grossièrement trois types d'entorses :

        <ul>
            <li>L'entorse bénigne ou entorse légère (foulure) avec ligaments distendus sans rupture. À ce stade, l’articulation est encore fonctionnelle.</li>
            <li>L’entorse moyenne : un étirement des ligaments accompagné d’un début de déchirement.</li>
            <li>L'entorse grave avec ligaments rompus ou arrachés.</li>
        </ul>
    </div>
    <div class="col-7"><img class="img-fluid" src="/images/osteopathie/indications/entorse.jpg" alt="ostéopathie et entorse" /></div>
</div>             

<div class="row mt-3">
    <div class="col-10">

        <p>Votre ostéopathe va s'assurer que l'entorse n'est pas une entorse grave nécessitant une radio. Dans le cas où une rupture ligamentaire ou 
            un arrachement osseux est suspecté, il vous ré-orientera vers les urgences ou vers votre médecin généraliste, et 
            reportera donc la séance d'ostéopathie une fois que le diagnostic aura été clairement établi.</p>

        <p>Dans le cas où l'entorse n'est pas considérée comme grave (ou après la phase d'immobilisation de l'entorse grave), l'ostéopathe va s'attacher à travailler sur plusieurs niveaux qui vont permettre la bonne cicatrisation des structures lésées, la restauration de la mobilité de l'articulation touchée et enfin, éviter la mise en place de compensations qui perturberaient l'équilibre de l'ensemble du corps (le dos qui se bloque 2 ans après, parce que l'ensemble du membre inférieur et le bassin ont compensé la perte de mobilité d'une cheville, par exemple). </p>

        <p>De manière générale, le travail de l'ostéopathe est de rétablir la mobilité de l'ensemble des structures du corps. Concrètement, dans le cas d'une entorse, il travaillera:</p>

        <ul>
            <li>sur la mobilité des surfaces articulaires de l'articulation lésée, mais également celle des articulations adjacentes qui ont aussi subi le traumatisme;</li>
            <li>sur les tissus péri-articulaires: les muscles et leurs tendons, les fascias, la capsule articulaire...;</li>
            <li>sur les systèmes vasculaire et nerveux à proximité et à distance de l'articulation: un bon apport sanguin favorisera une bonne cicatrisation des tissus (en apportant tous les substances qui y sont nécessaires), tout comme une bonne innervation...</li>
        </ul>

        <p>L'ensemble de ce travail aura un intérêt triple :</p>

        <ul>
            <li>une diminution de la douleur, en permettant une bonne circulation des liquides de l'articulation;</li>
            <li>une récupération plus rapide,</li>
            <li>éviter la mise en place de compensations, qui peuvent se manifester plus tard sous la forme de maux divers et variés (lumbago, douleurs articulaires, crampes, arthrose précoce, migraines...).</li>
        </ul>


    </div>
