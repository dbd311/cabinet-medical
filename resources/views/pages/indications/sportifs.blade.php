<p>
    <strong>Quand consulter un ostéopathe ?</strong>                
</p>
<ul>
    <li><strong>A titre curatif :</strong> l'ostéopathe agit sur les douleurs du sportif et travailler à la mobilité de son corps afin de rendre sa pratique plus efficace.</li>
    <li><strong>A titre préventif :</strong> l'ostéopathe travaille sur la posture et le bon équilibre du corps. Il donne aussi des conseils visant à préparer celui-ci à la reprise d’une compétition.</li>
    <li><strong>Pour la récupération physique :</strong> l’effort provoque de multiples contraintes sur le corps du sportif… Après une compétition ou un effort physique intense, le travail de l'ostéopathe stimule le drainage en travaillant sur les tensions accumulées. Ce travail s'effectue en collaboration avec le kinésithérapeute et permet une meilleure récupération ...</li>
</ul>
