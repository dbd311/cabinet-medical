<div class="section-title"><a href="/indications/osteopathie-et-enfants"> L'ostéopathie pour les enfants </a></div>

<div class="row">
    <div class="col-7"> 
        <p>L’équilibre général peut être perturbé par la croissance ou les activités de votre enfant. Le poids du cartable, une mauvaise posture à l’école, le port de semelles orthopédiques, la pose d'un appareil dentaire, un traumatisme au sport ou une chute ... sont autant de dysfonctionnement qui peuvent l’affecter durablement. Par exemple, le pouce dans la bouche ou la pose d’un appareil dentaire peut entraîner un déséquilibre de la fonction crânienne et favoriser les maux de tête, l’irritabilité, les troubles du sommeil ou de concentration de votre enfant.</p>
        <p>
            <strong>Dans tous les cas, apprenez à observer votre enfant et à l’écouter. Une plainte répétée doit vous alerter.</strong>
        </p>
    </div>
    <div class="col-4"><img class="img-fluid" src="/images/osteopathie/indications/osteo-et-enfant.jpg" alt="ostéopathie et enfant" /></div>
</div>

<p><strong>Il est conseillé de consulter votre ostéopathe en cas de :</strong></p>

<ul>
    <li>difficultés dans l’apprentissage de la marche, mauvaise position des pieds</li>
    <li>douleurs articulaires des membres ou de mal de dos</li>
    <li>déviation de la colonne vertébrale</li>
    <li>troubles digestifs (constipation, diarrhée, ballonnements…)</li>
    <li>troubles ORL à répétition (otite, bronchite, sinusite / rhinite…)</li>
    <li>suite à un traumatisme (chute, choc, entorse…)</li>
    <li>maux de tête, fatigue oculaire</li>
    <li>traitement orthodontique ou après la succion du pouce / doigts</li>
    <li>troubles du sommeil</li>
    <li>troubles de concentration, retard du développement psycho-moteur…</li>
    <li>pour faire un bilan annuel</li>                
</ul>
<p>A noter que l’ostéopathie agit sur les troubles fonctionnels de votre enfant : cela ne remplace ni le médecin traitant ni le pédiatre ou tout autre spécialiste nécessaire à votre enfant. Au contraire cela permet un suivit complémentaire.</p>


