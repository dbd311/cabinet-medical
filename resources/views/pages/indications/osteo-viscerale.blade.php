<div class="section-title"><a href="/indications/osteo-viscerale">Ostéopathie viscérale</a></div>

<div class="row m-b-1">
    <div class="col-xs-12"> 
        <p><strong>L’ostéopathie viscérale</strong> a pour but l’investigation des <strong>organes</strong> (foie, estomac, intestins, rate, reins, cœur ...), des viscères (estomac, intestin grêle…), mais également des membranes en rapport avec les muscles et <strong>viscères</strong> du corps humain : les fasciae.</p>
        <p>Le concept d’ostéopathie viscérale est repris par Barral et Mercier où ils y décrivent trois types de mouvements propres au sein de l’abdomen : <em>motricité</em>, <em>mobilité</em> et <em>motilité</em>.</p>
    </div>
    <div class="col-xs-12"><img class="img-fluid" src="/images/osteopathie/indications/osteo-viscerale.jpg" alt="ostéopathie viscérale" /></div>
</div>

<ul>
    <li><strong>La motricité :</strong> c’est le mouvement passif des organes lié aux modifications cinétiques du corps humain sous l’influence du système locomoteur, résultant lui-même du système nerveux central : SNC (activité physique, torsion du tronc ...).</li>
    <li><strong>La mobilité :</strong> c’est un mouvement automatique organique sous couvert du système nerveux végétatif : SNV. Ses automatismes sont « le mouvement respiratoire diaphragmatique», « le mouvement cardiaque» et « le péristaltisme organique ».</li>
    <li><strong>La motilité :</strong> c’est un phénomène intrinsèque lent et de faible amplitude. Ce mouvement est totalement indépendant des influences extérieures.</li>
</ul>

<p class="text-info"><strong>Les motifs de consultation</strong></p>
<p>L’ostéopathie viscérale est le plus souvent utilisée lors de motifs de consultation qui s’avèrent être liés à des troubles digestifs, comme par exemple :</p>
<ul>
    <li>Cervicalgies,</li>
    <li>Dorsalgies,</li>
    <li>Lombalgies,</li>
    <li>Migraines ou maux de tête,</li>
    <li>Douleurs abdominales,</li>
    <li>Constipation,</li>
    <li>Diarrhée,</li>
    <li>Reflux gastro oesophagien,</li>
    <li>Hémorroïdes,</li>
    <li>Troubles hépato-biliaires (foie et vésicule biliaire),</li>
    <li>Troubles du cycle menstruel ou de la ménopause, ,</li>
    <li>Latéroversion utérine ...</li>
</ul>

<p>Un équilibre nerveux retrouvé peut favoriser un retour à la normalité dans les cas :</p>
<ul>
    <li>d'anxiété,</li>
    <li>d'états dépressifs passagers, </li>
    <li>de troubles du sommeil, </li>
    <li>de stress: irritabilité, nervosité, angoisses, oppression ...</li>
</ul>

