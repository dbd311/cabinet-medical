<div class="row">
    <div class="col-8"> 
        <p>            
        À l'occasion de la 11<sup>ème</sup> édition de la semaine pour la qualité de vie au travail qui avait lieu du 16 au 20 juin 2014, 
        l'<a href="http://www.osteofrance.com/ufof/presentation/" target="_blank">Union Fédérale des Ostéopathes de France</a> (UFOF), 
        rappelle que l'Ostéopathie est une pratique qui permet de retrouver l'équilibre du corps face aux diverses contraintes imposées par le travail.
        </p>  
        
        <h4>Un bon équilibre du corps pour le bien-être au travail</h4> 
        
        <p>Quels que soient le métier et le secteur d'activité, chacun passe quotidiennement de nombreuses heures dans la même posture ou à répéter le 
            même mouvement. Le stress et les efforts du quotidien s'ajoutent et provoquent le déséquilibre du corps. 
            En effet, l'anxiété répétée engendre les maux de tête, la perte de sommeil, les troubles de la digestion, etc. 
            On observe également une baisse de moral et l'apparition de troubles musculo-squelettiques.</p>
        
        <h4>La prise en charge ostéopathique</h4>
        
        <p>Toutes ces pathologies, en plus d'avoir des conséquences importantes sur la santé, affectent fortement la productivité des individus. 
            C'est une des raisons pour laquelle l'UFOF souhaite faire passer le message suivant : 
            les actifs devraient spontanément et plus régulièrement consulter un ostéopathe. 
            En effet, à l'image des révisions régulières pour nos voitures, la prise en charge préventive et régulière des tensions liées au travail et 
            au stress permet d'éviter de nombreuses complications et d'enrayer l'apparition de maladies professionnelles.</p> 
        <p>Le ressenti des premières douleurs et tensions doit inciter le patient à consulter un ostéopathe. 
            Ce dernier est en mesure de dresser un bilan global afin de faire le point sur l'équilibre du corps. 
            Il offre aussi des solutions pour permettre aux patients de faire face rapidement aux contraintes que son corps subit quotidiennement.
        </p>
        <h4>Rappel de la pratique ostéopathique</h4> 
        <p>L'ostéopathie est une approche thérapeutique manuelle préventive et curative qui s'emploie à déterminer et à traiter les restrictions de mobilité qui peuvent affecter l'ensemble des structures composant le corps humain. Toute perte de mobilité des articulations, des muscles, des ligaments ou des viscères provoque des déséquilibres de l'état de santé. Grâce à des tests palpatoires spécifiques, l'ostéopathe trouve les zones du corps présentant des restrictions de mouvements susceptibles d'altérer l'état de santé. L'ostéopathie considère l'ensemble du corps et non uniquement le symptôme, car l'origine du problème se trouve souvent ailleurs que l'endroit où la souffrance se manifeste.</p>
